    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombre = tx_nombre.getText().trim();
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No se ingreso el nombre");
            return;
        }
        String contrasena = String.valueOf(jPasswordField1.getPassword());

        if (contrasena.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No se ingreso la contrasena");
            return;
        }

        tx_nombre.setText(" ");
        jPasswordField1.setText("");
        try {
            
            AccesoAleatorio.crearJugador(new File("Jugadores.txt"));
            int i = AccesoAleatorio.buscarRegistroExist(nombre, contrasena);
            if (i == -1) {
                JOptionPane.showMessageDialog(null, "No coincide con un registro");
                return;
            }

            AccesoAleatorio.cerrar();
            PantallaInstrucciones jugar = new PantallaInstrucciones();
            jugar.setVisible(true);
            jugar.setTitle( "Pantalla de Intrucciones");
            dispose();

        } catch (Exception e) {
                  JOptionPane.showMessageDialog(null, "Error en la busqueda");
        }

        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

